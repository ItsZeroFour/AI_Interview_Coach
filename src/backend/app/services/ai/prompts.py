def build_question_prompt(
    resume_text: str,
    job_description: str,
    question_type: str,
    question_number: int,
    total_questions: int,
    chat_history: str = "",
) -> str:
    history_block = ""
    if chat_history:
        history_block = f"\n\n## Предыдущий диалог:\n{chat_history}"

    type_descriptions = {
        "behavioral": "вопрос о конкретном опыте кандидата (STAR-формат: Situation, Task, Action, Result)",
        "technical": "проверка технических знаний по навыкам из резюме и требованиям вакансии",
        "situational": "гипотетическая рабочая ситуация, требующая принятия решения",
    }

    type_desc = type_descriptions.get(question_type, question_type)

    return f"""## Резюме кандидата:
{resume_text[:3000]}

## Описание вакансии:
{job_description[:2000]}
{history_block}

{FEW_SHOT_QUESTIONS}

## Задание:
Сгенерируй вопрос #{question_number} из {total_questions} для mock-собеседования.
Тип вопроса: {question_type} — {type_desc}.

Требования к вопросу:
1. Должен учитывать конкретные навыки или опыт из резюме
2. Должен соотноситься с требованиями вакансии
3. Не повторяй предыдущие вопросы из диалога
4. Должен быть конкретным и открытым (не да/нет)
5. Длина: 1-3 предложения

Верни ТОЛЬКО текст вопроса на русском языке, без нумерации, кавычек и пояснений."""


def build_evaluation_prompt(
    question: str,
    answer: str,
    job_description: str,
) -> str:
    return f"""## Вопрос интервьюера:
{question}

## Ответ кандидата:
{answer}

## Описание вакансии:
{job_description[:1500]}

## Задание:
Проанализируй ответ по шагам:
1. Релевантен ли ответ вопросу?
2. Использует ли кандидат конкретные примеры, цифры, факты?
3. Есть ли структура (STAR)?
4. Соответствует ли уровень ответа требованиям вакансии?
5. Что можно улучшить?

Затем верни оценку строго в JSON:

{{
  "score": <число от 1 до 5>,
  "strengths": ["сильная сторона 1", "сильная сторона 2"],
  "improvements": ["конкретный совет 1", "конкретный совет 2"],
  "comment": "краткий комментарий с рекомендацией (1-2 предложения)"
}}

Шкала оценки:
- 5: отличный ответ — конкретные примеры, цифры, чёткая STAR-структура, явный результат
- 4: хороший ответ — есть примеры и логика, но не хватает деталей или измеримых результатов
- 3: средний ответ — общие фразы, мало конкретики, слабая структура
- 2: слабый ответ — поверхностно, не по теме или клише
- 1: ответ отсутствует, бессмысленный или полностью нерелевантный

Верни ТОЛЬКО JSON, без markdown и пояснений."""


QUESTION_TYPE_SEQUENCE = {
    "mixed": [
        "behavioral",
        "technical",
        "situational",
        "technical",
        "behavioral",
        "technical",
        "situational",
        "behavioral",
        "technical",
        "situational",
    ],
    "behavioral": ["behavioral"] * 10,
    "technical": ["technical"] * 10,
}


def get_question_type(interview_type: str, question_number: int) -> str:
    sequence = QUESTION_TYPE_SEQUENCE.get(interview_type, QUESTION_TYPE_SEQUENCE["mixed"])
    idx = (question_number - 1) % len(sequence)
    return sequence[idx]


def build_report_prompt(
    resume_text: str,
    job_description: str,
    qa_pairs: list[dict],
) -> str:
    qa_text = ""
    for i, pair in enumerate(qa_pairs, 1):
        qa_text += f"\nВопрос {i}: {pair['question']}\n"
        qa_text += f"Ответ: {pair['answer']}\n"
        if pair.get('score'):
            qa_text += f"Оценка: {pair['score']}/5\n"

    return f"""## Резюме кандидата:
{resume_text[:2000]}

## Вакансия:
{job_description[:1500]}

## Пройденное собеседование:
{qa_text}

## Задание:
Сформируй итоговый отчёт по собеседованию. Оцени кандидата по 5 ключевым компетенциям.
Каждая оценка должна быть обоснована конкретными ответами кандидата.

Верни строго JSON:

{{
  "overall_score": <число от 1 до 5>,
  "competencies": [
    {{"name": "Коммуникация", "score": <1-5>}},
    {{"name": "Технические навыки", "score": <1-5>}},
    {{"name": "Решение проблем", "score": <1-5>}},
    {{"name": "Релевантный опыт", "score": <1-5>}},
    {{"name": "Мотивация", "score": <1-5>}}
  ],
  "summary": "общий вывод о готовности кандидата к этой вакансии (3-4 предложения)",
  "strengths": ["конкретная сильная сторона 1", "сильная сторона 2", "сильная сторона 3"],
  "improvements": ["конкретная зона роста 1", "зона роста 2", "зона роста 3"]
}}

Верни ТОЛЬКО JSON."""


